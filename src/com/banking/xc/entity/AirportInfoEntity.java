package com.banking.xc.entity;

public class AirportInfoEntity {
	private String Airport;
	private String AirportName;
	private String AriportEnName;
	private String ShortName;
	private String CityId;
	private String CityName;
	public String getAirport() {
		return Airport;
	}
	public void setAirport(String airport) {
		Airport = airport;
	}
	public String getAirportName() {
		return AirportName;
	}
	public void setAirportName(String airportName) {
		AirportName = airportName;
	}
	public String getAriportEnName() {
		return AriportEnName;
	}
	public void setAriportEnName(String ariportEnName) {
		AriportEnName = ariportEnName;
	}
	public String getShortName() {
		return ShortName;
	}
	public void setShortName(String shortName) {
		ShortName = shortName;
	}
	public String getCityId() {
		return CityId;
	}
	public void setCityId(String cityId) {
		CityId = cityId;
	}
	public String getCityName() {
		return CityName;
	}
	public void setCityName(String cityName) {
		CityName = cityName;
	}
	
}
