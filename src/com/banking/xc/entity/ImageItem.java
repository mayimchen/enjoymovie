package com.banking.xc.entity;

public class ImageItem {
	private String category;
	private String imageUrl;
	private String descriptCaption;
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getImageUrl() {
		return imageUrl;
	}
	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}
	public String getDescriptCaption() {
		return descriptCaption;
	}
	public void setDescriptCaption(String descriptCaption) {
		this.descriptCaption = descriptCaption;
	}
	
	
}
